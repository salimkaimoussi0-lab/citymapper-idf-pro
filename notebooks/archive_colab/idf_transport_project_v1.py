# On bloque l'indice maximum au 95ème centile pour une belle carte
limite_haute = df_carte['INDICE_TENSION'].quantile(0.95)
df_carte['INDICE_AFFICHE'] = df_carte['INDICE_TENSION'].clip(upper=limite_haute)

